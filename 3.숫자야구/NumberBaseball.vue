    </form>
    <div>시도 {{tries.length}}</div>
    <ul>
      <li v-for="t in tries">
        <div>{{t.try}}</div>
        <div>{{t.result}}</div>
      </li>
    </ul>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        answer: geNumbers(),
        tries: [],
